package dao;

import com.mycompany.gp.domain.PRODUCT_SIZE;
import com.mycompany.gp.domain.Product;
import dao.exceptions.NonexistentEntityException;
import java.io.Serializable;
import java.util.List;
import javax.persistence.EntityManager;
import javax.persistence.EntityManagerFactory;
import javax.persistence.EntityNotFoundException;
import javax.persistence.Query;
import javax.persistence.TypedQuery;
import javax.persistence.criteria.CriteriaQuery;
import javax.persistence.criteria.Root;

/**
 *
 * @author waw
 */
public class ProductDAO implements IProductDAO {

    public ProductDAO() {

    }
    public EntityManager getEntityManager() {
        return EntityManagerFactorySingleton.getInstance().createEntityManager();
    }

    public void create(Product product) {
        EntityManager em = null;
        try {
            em = getEntityManager();
            em.getTransaction().begin();
            em.persist(product);
            em.getTransaction().commit();
        } finally {
            if (em != null) {
                em.close();
            }
        }
    }

    public void edit(Product product) throws NonexistentEntityException, Exception {
        EntityManager em = null;
        try {
            em = getEntityManager();
            em.getTransaction().begin();
            product = em.merge(product);
            em.getTransaction().commit();
        } catch (Exception ex) {
            String msg = ex.getLocalizedMessage();
            if (msg == null || msg.length() == 0) {
                long id = product.getId();
                if (findProduct(id) == null) {
                    throw new NonexistentEntityException("The product with id " + id + " no longer exists.");
                }
            }
            throw ex;
        } finally {
            if (em != null) {
                em.close();
            }
        }
    }

    public void destroy(long id) throws NonexistentEntityException {
        EntityManager em = null;
        try {
            em = getEntityManager();
            em.getTransaction().begin();
            Product product;
            try {
                product = em.getReference(Product.class, id);
                product.getId();
            } catch (EntityNotFoundException enfe) {
                throw new NonexistentEntityException("The product with id " + id + " no longer exists.", enfe);
            }
            em.remove(product);
            em.getTransaction().commit();
        } finally {
            if (em != null) {
                em.close();
            }
        }
    }

    public List<Product> findProductEntities() {
        return findProductEntities(true, -1, -1);
    }

    public List<Product> findProductEntities(int maxResults, int firstResult) {
        return findProductEntities(false, maxResults, firstResult);
    }

    private List<Product> findProductEntities(boolean all, int maxResults, int firstResult) {
        EntityManager em = getEntityManager();
        try {
            CriteriaQuery cq = em.getCriteriaBuilder().createQuery();
            cq.select(cq.from(Product.class));
            Query q = em.createQuery(cq);
            if (!all) {
                q.setMaxResults(maxResults);
                q.setFirstResult(firstResult);
            }
            return q.getResultList();
        } finally {
            em.close();
        }
    }

    public Product findProduct(long id) {
        EntityManager em = getEntityManager();
        try {
            return em.find(Product.class, id);
        } finally {
            em.close();
        }
    }

    public int getProductCount() {
        EntityManager em = getEntityManager();
        try {
            CriteriaQuery cq = em.getCriteriaBuilder().createQuery();
            Root<Product> rt = cq.from(Product.class);
            cq.select(em.getCriteriaBuilder().count(rt));
            Query q = em.createQuery(cq);
            return ((Long) q.getSingleResult()).intValue();
        } finally {
            em.close();
        }
    }

    @Override
    public Product findProductByName(String name) {
      EntityManager em = getEntityManager();
      String jpql = "Select p FROM Product p WHERE p.name = :name ORDER BY p.price ASC";
      TypedQuery<Product> query = em.createQuery(jpql, Product.class);
      query.setParameter("name", name);
      query.setMaxResults(1);
      return query.getSingleResult();
    }

    @Override
    public Product findSmallProduct(String name) {
      EntityManager em = getEntityManager();
      String jpql = "Select p FROM Product p WHERE p.name = :name AND p.PRODUCT_SIZE = :PRODUCT_SIZE";
      TypedQuery<Product> query = em.createQuery(jpql, Product.class);
      query.setParameter("name", name);
      query.setParameter("PRODUCT_SIZE", PRODUCT_SIZE.SMALL);
      return query.getSingleResult();
    }

    @Override
    public Product findMediumProduct(String name) {
      EntityManager em = getEntityManager();
      String jpql = "Select p FROM Product p WHERE p.name = :name AND p.PRODUCT_SIZE = :PRODUCT_SIZE";
      TypedQuery<Product> query = em.createQuery(jpql, Product.class);
      query.setParameter("name", name);
      query.setParameter("PRODUCT_SIZE", PRODUCT_SIZE.MEDIUM);
      return query.getSingleResult();
    }

    @Override
    public Product findLargeProduct(String name) {
      EntityManager em = getEntityManager();
      String jpql = "Select p FROM Product p WHERE p.name = :name AND p.PRODUCT_SIZE = :PRODUCT_SIZE";
      TypedQuery<Product> query = em.createQuery(jpql, Product.class);
      query.setParameter("name", name);
      query.setParameter("PRODUCT_SIZE", PRODUCT_SIZE.LARGE);
      return query.getSingleResult();
    }
    
    
    
    
}
